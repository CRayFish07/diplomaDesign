package com.toy.model;

public class UserLog {
	private int log_id;
	private String log_name;
	private String log_password;
	private int log_emp_id;
	
	// getter && setter
	public int getLog_id() {
		return log_id;
	}
	public void setLog_id(int logId) {
		log_id = logId;
	}
	public String getLog_name() {
		return log_name;
	}
	public void setLog_name(String logName) {
		log_name = logName;
	}
	public String getLog_password() {
		return log_password;
	}
	public void setLog_password(String logPassword) {
		log_password = logPassword;
	}
	public int getLog_emp_id() {
		return log_emp_id;
	}
	public void setLog_emp_id(int logEmpId) {
		log_emp_id = logEmpId;
	}
	
	
}
