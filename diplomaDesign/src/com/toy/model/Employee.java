package com.toy.model;

public class Employee {
	private int emp_id;
	private String emp_name;
	private String emp_sex;
	private String emp_phone_number;
	private int emp_QQ;
	private String emp_company;
	private String emp_email;
	private String emp_address;
	private int emp_dept_id;
	private int emp_authority;
	
	//getter && setter
	public int getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(int empId) {
		emp_id = empId;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String empName) {
		emp_name = empName;
	}
	public String getEmp_sex() {
		return emp_sex;
	}
	public void setEmp_sex(String empSex) {
		emp_sex = empSex;
	}
	public String getEmp_phone_number() {
		return emp_phone_number;
	}
	public void setEmp_phone_number(String empPhoneNumber) {
		emp_phone_number = empPhoneNumber;
	}
	public int getEmp_QQ() {
		return emp_QQ;
	}
	public void setEmp_QQ(int empQQ) {
		emp_QQ = empQQ;
	}
	public String getEmp_company() {
		return emp_company;
	}
	public void setEmp_company(String empCompany) {
		emp_company = empCompany;
	}
	public String getEmp_email() {
		return emp_email;
	}
	public void setEmp_email(String empEmail) {
		emp_email = empEmail;
	}
	public String getEmp_address() {
		return emp_address;
	}
	public void setEmp_address(String empAddress) {
		emp_address = empAddress;
	}
	public int getEmp_dept_id() {
		return emp_dept_id;
	}
	public void setEmp_dept_id(int empDeptId) {
		emp_dept_id = empDeptId;
	}
	public int getEmp_authority() {
		return emp_authority;
	}
	public void setEmp_authority(int empAuthority) {
		emp_authority = empAuthority;
	}
	
}
