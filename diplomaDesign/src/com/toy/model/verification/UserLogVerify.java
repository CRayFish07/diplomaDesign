package com.toy.model.verification;

public class UserLogVerify {
	private int emp_authority;
	private String log_password;
	
	// getter && setter
	public int getEmp_authority() {
		return emp_authority;
	}
	public void setEmp_authority(int empAuthority) {
		emp_authority = empAuthority;
	}
	public String getLog_password() {
		return log_password;
	}
	public void setLog_password(String logPassword) {
		log_password = logPassword;
	}

}