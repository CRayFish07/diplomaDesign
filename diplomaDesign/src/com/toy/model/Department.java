package com.toy.model;

public class Department {
	private int dept_id;
	private String dept_name;
	
	// getter && setter
	public int getDept_id() {
		return dept_id;
	}
	public void setDept_id(int deptId) {
		dept_id = deptId;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String deptName) {
		dept_name = deptName;
	}
	
	

}
